package br.com.transferr.core.metadata;

public class CoordinatesQuadrant {

	private Coordinates coordFarRight;
	private Coordinates coordFarLeft;
	private Coordinates coordNearRight;
	private Coordinates coordNearLeft;
	public Coordinates getCoordFarRight() {
		return coordFarRight;
	}
	public void setCoordFarRight(Coordinates coordFarRight) {
		this.coordFarRight = coordFarRight;
	}
	public Coordinates getCoordFarLeft() {
		return coordFarLeft;
	}
	public void setCoordFarLeft(Coordinates coordFarLeft) {
		this.coordFarLeft = coordFarLeft;
	}
	public Coordinates getCoordNearRight() {
		return coordNearRight;
	}
	public void setCoordNearRight(Coordinates coordNearRight) {
		this.coordNearRight = coordNearRight;
	}
	public Coordinates getCoordNearLeft() {
		return coordNearLeft;
	}
	public void setCoordNearLeft(Coordinates coordNearLeft) {
		this.coordNearLeft = coordNearLeft;
	}
	
}
